time = [list(input()) for _ in range(5)]
times = []
for i in range(4):
    now = []
    for line in range(5):
        now.append([time[line][i * 4], time[line][i * 4 + 1], time[line][i * 4 + 2]])
    times.append(now)

result = []
for time in times:
    for index in range(len(numbers)):
        isPossible = True
        for i in range(5):
            if not isPossible:
                break
            for j in range(3):
                if not isPossible:
                    break
                if time[i][j] == '#' and numbers[index][i][j] == '.':
                    isPossible = False
        if isPossible:
            result.append(index)
            break

